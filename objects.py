from __future__ import unicode_literals


class Object(dict):
    def __init__(self, **kwargs):
        for k, v in kwargs.items():
            if v is not None:
                self[k] = v
        self.__dict__ = self

    def __getattr__(self, name):
        return None

    def __delattr__(self, name):
        self.pop(name, None)
